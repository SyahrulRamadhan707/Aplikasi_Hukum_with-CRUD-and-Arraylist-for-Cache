    private void btnCariNamaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariNamaActionPerformed
        String keyword = txtCariNama.getText().trim();

        if (keyword.isEmpty()) {
            rowSorterClient.setRowFilter(null); // tampilkan semua data
        } else {
            // Buat filter untuk seluruh kolom
            RowFilter<DefaultTableModel, Object> filter = new RowFilter<DefaultTableModel, Object>() {
                public boolean include(Entry<? extends DefaultTableModel, ? extends Object> entry) {
                    for (int i = 0; i < entry.getValueCount(); i++) {
                        if (entry.getStringValue(i).toLowerCase().contains(keyword.toLowerCase())) {
                            return true;
                        }
                    }
                    return false;
                }
            };
            rowSorterClient.setRowFilter(filter);
        }
    }//GEN-LAST:event_btnCariNamaActionPerformed

