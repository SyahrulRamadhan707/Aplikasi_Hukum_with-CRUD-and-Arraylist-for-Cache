    private void tblClientMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientMouseClicked
        int selectedRow = tblClient.getSelectedRow();
        if (selectedRow != -1) {
            btnUbah.setEnabled(true);
            btnHapus.setEnabled(true);
            
            txtId.setText(modelClient.getValueAt(selectedRow, 0).toString());
            txtNik.setText(modelClient.getValueAt(selectedRow, 1).toString());
            txtNm.setText(modelClient.getValueAt(selectedRow, 2).toString());
            txtTmpt.setText(modelClient.getValueAt(selectedRow, 3).toString());
            String tglLahir = modelClient.getValueAt(selectedRow, 4).toString();
            if (tglLahir != null && !tglLahir.trim().isEmpty()) {
                try {
                    DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
                    Date date = df.parse(tglLahir);
                    dtTglLhr.setDate(date);
                } catch (ParseException e) {
                    e.printStackTrace();
                    dtTglLhr.setDate(null); 
                }
            } else {
                dtTglLhr.setDate(null); 
            }
            cbJk.setSelectedItem(modelClient.getValueAt(selectedRow, 5).toString());
            cbAgm.setSelectedItem(modelClient.getValueAt(selectedRow, 6).toString());
            txtAlm.setText(modelClient.getValueAt(selectedRow, 7).toString());
            cbSts.setSelectedItem(modelClient.getValueAt(selectedRow, 8).toString());
            txtKs.setText(modelClient.getValueAt(selectedRow, 9).toString());
            try {
                String tglKasus = modelClient.getValueAt(selectedRow, 10).toString();
                DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
                dtTglKss.setDate(df.parse(tglKasus));
            } catch (ParseException e) {
                e.printStackTrace();
            }
            txtLks.setText(modelClient.getValueAt(selectedRow, 11).toString());
            txtUr.setText(modelClient.getValueAt(selectedRow, 12).toString());
        }
    }//GEN-LAST:event_tblClientMouseClicked

