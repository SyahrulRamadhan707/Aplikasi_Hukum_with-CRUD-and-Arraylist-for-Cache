    private void tblDaftarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDaftarMouseClicked
        int selectedRow = tblDaftar.getSelectedRow();
        if (selectedRow != -1) {
            btnUbah2.setEnabled(true);
            btnHapus2.setEnabled(true);
            
            txtId2.setText(modelDaftar.getValueAt(selectedRow, 0).toString());
            txtNik2.setText(modelDaftar.getValueAt(selectedRow, 1).toString());
            txtNm2.setText(modelDaftar.getValueAt(selectedRow, 2).toString());
            txtTmpt2.setText(modelDaftar.getValueAt(selectedRow, 3).toString());
            String tglLahir = modelDaftar.getValueAt(selectedRow, 4).toString();
            if (tglLahir != null && !tglLahir.trim().isEmpty()) {
                try {
                    DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
                    Date date = df.parse(tglLahir);
                    dtTglLhr2.setDate(date);
                } catch (ParseException e) {
                    e.printStackTrace();
                    dtTglLhr2.setDate(null); 
                }
            } else {
                dtTglLhr2.setDate(null); 
            }
            cbJk2.setSelectedItem(modelDaftar.getValueAt(selectedRow, 5).toString());
            cbAgm3.setSelectedItem(modelDaftar.getValueAt(selectedRow, 6).toString());
            txtAlm3.setText(modelDaftar.getValueAt(selectedRow, 7).toString());
            txtJwb.setText(modelDaftar.getValueAt(selectedRow, 8).toString());
            txtPkr.setText(modelDaftar.getValueAt(selectedRow, 9).toString());
            cbSts1.setSelectedItem(modelDaftar.getValueAt(selectedRow, 10).toString());
            cbTndk.setSelectedItem(modelDaftar.getValueAt(selectedRow, 11).toString());
            cbHsl.setSelectedItem(modelDaftar.getValueAt(selectedRow, 12).toString());
        }
    }//GEN-LAST:event_tblDaftarMouseClicked

